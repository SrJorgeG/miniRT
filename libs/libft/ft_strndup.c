#include "libft.h"
#include <stdlib.h>

char	*ft_strndup(const char *s, size_t x)
{
	size_t	i;
	size_t	len;
	char	*dup;

	i = 0;
	len = 0;
	if (!s)
		return (NULL);
	while (s[len] && len < x)
		len++;
	dup = (char *)malloc(sizeof(char) * (len + 1));
	if (!dup)
		return (NULL);
	while (i < len)
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
